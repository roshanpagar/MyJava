package Inheritance.parentchild;

public class MyTime extends MyDate {
	int hrs,min,secs;
	
	public int getHrs()
	{
		return hrs;
	}
	public void setHrs(int hrs)
	{
		this.hrs=hrs;
	}
	
	public int getMin()
	{
		return min;
	}
	public void setmin(int min)
	{
		this.min=min;
	}
	
	public int getSecs()
	{
		return secs;
	}
	public void setSecs(int secs)
	{
		this.secs=secs;
	}

}
